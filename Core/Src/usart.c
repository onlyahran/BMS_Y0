void USART_Init(void)
{
	MX_USART1_UART_Init(); // ll driver
	MX_USART2_UART_Init(); // hal driver

	uart_hdr_g = 0;
	uart_tail_g = 0;

	LL_USART_EnableIT_RXNE(USART2);
}

void UART_Rx_Callback(USART_TypeDef *USARTx)
{
	uint8_t data;

	if(LL_USART_IsActiveFlag_ORE(USARTx))
	{
		LL_USART_ClearFlag_ORE(USARTx);
	}

	if(LL_USART_IsActiveFlag_RXNE(USARTx))
	{
		data = LL_USART_ReceiveData8(USARTx);
		uartBuffer_g[uart_hdr_g] = data;
		uart_hdr_g++;
		uart_hdr_g %= UART_BUFFER_LEN;
	}
}

void UART_TX(USART_TypeDef *USARTx, uint8_t *pBuff, uint16_t size)
{
	for(int i=0; i<size; i++)
	{
		while(!LL_USART_IsActiveFlag_TXE(USARTx));
		LL_USART_TransmitData8(USARTx, *pBuff++);
	}
}
